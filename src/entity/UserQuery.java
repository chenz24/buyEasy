package entity;

public class UserQuery {
	private String username;
	private int p;
	private int num;
	public int getP() {
		return p;
	}

	public void setP(int p) {
		this.p = p;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}
	
}
